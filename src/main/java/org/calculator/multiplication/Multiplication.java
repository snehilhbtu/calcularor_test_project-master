package org.calculator.multiplication;

public class Multiplication {
    public static void main(String[] args) {
        int j = do_multiplication(5, 6);
        System.out.println(j);
    }

    public static int do_multiplication(int i, int i1) {
        return i * i1;
    }

    public static double do_multiplication(long i, long i1) {
        return i * i1;
    }

    public static double do_multiplication(float i, float i1) {
        return i * i1;
    }
}
