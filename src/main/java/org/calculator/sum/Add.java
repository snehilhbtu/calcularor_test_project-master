package org.calculator.sum;

public class Add {
    public static void main(String[] args) {
        int j = do_add(5, 6);
        System.out.println(j);
    }

    public static int do_add(int i, int i1) {
        return i + i1;
    }

    public static double add(double num1, double num2) {
        return num1 + num2;
    }
}
